    public void tampil() {
        DefaultTableModel tabelhead = new DefaultTableModel();
        tabelhead.addColumn("Nama Gudang");
        tabelhead.addColumn("Nama Barang");
        tabelhead.addColumn("Kapasitas Barang");
        tabelhead.addColumn("Stok Minimum");
        tabelhead.addColumn("Stok Bahan");
        try {
            koneksi();
            String sql = "SELECT * FROM gbp";
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelhead.addRow(new Object[]{res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6),});
            }
            jTable1.setModel(tabelhead);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "BELUM TERKONEKSI");
        }
    }

