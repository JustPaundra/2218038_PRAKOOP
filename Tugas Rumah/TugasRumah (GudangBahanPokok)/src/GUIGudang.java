    public void insert() {
        String NamaGudang = jTextField1.getText();
        String NamaBarang = jTextField3.getText();
        int KapasitasBarang = Integer.parseInt(jTextField2.getText());
        int StokMinimum = Integer.parseInt(jTextField8.getText());
        int StokBahan = Integer.parseInt(jTextField9.getText());
        try {
            koneksi();
            Statement statement = conn.createStatement();
            statement.executeUpdate("INSERT INTO gbp (NamaGudang, NamaBarang, KapasitasBarang, StokMinimum, StokBahan)"
                    + "VALUES('" + NamaGudang + "','" + NamaBarang + "','" + KapasitasBarang + "','" + StokMinimum + "','" + StokBahan + "')");
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data Gudang!" + "\n");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Input!");
        }
        refresh();
    }

    public void update() {
        String NamaGudang = jTextField1.getText();
        String NamaBarang = jTextField3.getText();
        int KapasitasBarang = Integer.parseInt(jTextField2.getText());
        int StokMinimum = Integer.parseInt(jTextField8.getText());
        int StokBahan = Integer.parseInt(jTextField9.getText());
        String namagudanglama = namagudang1;
        try {
            Statement statement = conn.createStatement();
            statement.executeUpdate("UPDATE gbp SET namagudang='" + NamaGudang + "'," + "namabarang='" + NamaBarang + "',"
                    + "kapasitasbarang='" + KapasitasBarang + "'" + ",stokminimum='" + StokMinimum + "',stokbahan='" + StokBahan + "' WHERE namagudang = '" + namagudanglama + "'");
            statement.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Update Data Gudang Berhasil!");
        } catch (Exception e) {
            System.out.println("Error : " + e);
        }
        refresh();
    }

    public void delete() {
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                String sql = "DELETE FROM gbp WHERE namagudang='" + jTextField1.getText() + "'";
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil di hapus");
                batal();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus");
            }
        }
        refresh();
    }

